# 1 (concatenation)
word1 = input("Enter first word: ")
word2 = input("Enter second word: ")

print("")

print(word1 + ", " + word2)

print("")

# 2 (format)
word1 = input("Enter first word: ")
word2 = input("Enter second word: ")

print("")

result = "{}, {}".format(word1, word2)

print(result)

print("")

# 3 (f)
word1 = input("Enter first word: ")
word2 = input("Enter second word: ")

print("")

result = f"{word1}, {word2}"

print(result)

print("")

# 4 (split)
words = input("Enter few words: ")

print("")

print(words.split(" "))

print("")


