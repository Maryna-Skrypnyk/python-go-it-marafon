# i набуває значення в діапазоні [0, 10)
for i in range(10):
    print('i =', i)

# i набуває значення в діапазоні [5, 10)
for i in range(5, 10):
    print('i =', i)

# i набуває значення в діапазоні [10, 101) з кроком 5
for i in range(10, 101, 5):
    print('i =', i)
print()

for i in range(100, -100, -5):
    print('i =', i)
